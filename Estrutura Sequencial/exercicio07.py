#Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
lado =  int(input('Informe a medida do lado do quadrado: '))
calculoArea = lado**2
dobroArea = calculoArea**2
print(f'área do quadrado  = {calculoArea}')
print(f' dobro da área do quadrado  = {dobroArea}')
